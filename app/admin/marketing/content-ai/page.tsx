import { redirect } from "next/navigation"
import { createClient } from "@/lib/supabase/server"
import AdminLayout from "../../admin-layout"
import { AIContentWriter } from "@/components/admin/marketing/ai-content-writer"

export default async function AIContentWriterPage() {
  const supabase = await createClient()
  const {
    data: { user },
  } = await supabase.auth.getUser()

  if (!user) {
    redirect("/login")
  }

  return (
    <AdminLayout>
      <AIContentWriter />
    </AdminLayout>
  )
}
